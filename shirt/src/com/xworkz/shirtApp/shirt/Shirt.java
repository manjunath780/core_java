package com.xworkz.shirtApp.shirt;

public class Shirt {

    private int shirtId;
    private String shirtBrand;
    private String shirtColor;
    private String shirtSize;
    private double shirtPrice;
    private String shirtFabric;
    private boolean isFormal;



    public int getShirtId() {
        return shirtId;
    }

    public void setShirtId(int shirtId) {
        this.shirtId = shirtId;
    }

    public String getShirtBrand() {
        return shirtBrand;
    }

    public void setShirtBrand(String shirtBrand) {
        this.shirtBrand = shirtBrand;
    }

    public String getShirtColor() {
        return shirtColor;
    }

    public void setShirtColor(String shirtColor) {
        this.shirtColor = shirtColor;
    }

    public String getShirtSize() {
        return shirtSize;
    }

    public void setShirtSize(String shirtSize) {
        this.shirtSize = shirtSize;
    }

    public double getShirtPrice() {
        return shirtPrice;
    }

    public void setShirtPrice(double shirtPrice) {
        this.shirtPrice = shirtPrice;
    }

    public String getShirtFabric() {
        return shirtFabric;
    }

    public void setShirtFabric(String shirtFabric) {
        this.shirtFabric = shirtFabric;
    }

    public boolean isFormal() {
        return isFormal;
    }

    public void setFormal(boolean isFormal) {
        this.isFormal = isFormal;
    }
}
