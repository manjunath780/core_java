package com.xworkz.inheritance.book;

public class Book {
    public void open() {
        System.out.println("Book is opened");
    }

    public void read() {
        System.out.println("Book is being read");
    }

    public void hasPages() {
        System.out.println("Book has multiple pages");
    }

    public void hasCover() {
        System.out.println("Book has a front and back cover");
    }

    public void printedContent() {
        System.out.println("Book contains printed content");
    }

}


