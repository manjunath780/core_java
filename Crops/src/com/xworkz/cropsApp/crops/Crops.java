package com.xworkz.cropsApp.crops;


public class Crops {

    private int cropId;
    private String cropName;
    private double cropArea;
    private String cropSeason;
    private String cropType;
    private double cropYield;
    private String cropRegion;

    public int getCropId() {
        return cropId;
    }

    public void setCropId(int cropId) {
        this.cropId = cropId;
    }

    public String getCropName() {
        return cropName;
    }

    public void setCropName(String cropName) {
        this.cropName = cropName;
    }

    public double getCropArea() {
        return cropArea;
    }

    public void setCropArea(double cropArea) {
        this.cropArea = cropArea;
    }

    public String getCropSeason() {
        return cropSeason;
    }

    public void setCropSeason(String cropSeason) {
        this.cropSeason = cropSeason;
    }

    public String getCropType() {
        return cropType;
    }

    public void setCropType(String cropType) {
        this.cropType = cropType;
    }

    public double getCropYield() {
        return cropYield;
    }

    public void setCropYield(double cropYield) {
        this.cropYield = cropYield;
    }

    public String getCropRegion() {
        return cropRegion;
    }

    public void setCropRegion(String cropRegion) {
        this.cropRegion = cropRegion;
    }
}
